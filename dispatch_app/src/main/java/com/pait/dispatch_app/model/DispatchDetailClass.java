package com.pait.dispatch_app.model;

public class DispatchDetailClass {

    private String CBL, noOfCartons, imgName;
    private int srNo;

    public String getCBL() {
        return CBL;
    }

    public void setCBL(String CBL) {
        this.CBL = CBL;
    }

    public String getNoOfCartons() {
        return noOfCartons;
    }

    public void setNoOfCartons(String noOfCartons) {
        this.noOfCartons = noOfCartons;
    }

    public String getImgName() {
        return imgName;
    }

    public void setImgName(String imgName) {
        this.imgName = imgName;
    }

    public int getSrNo() {
        return srNo;
    }

    public void setSrNo(int srNo) {
        this.srNo = srNo;
    }
}
